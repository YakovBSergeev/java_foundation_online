package ru.itsjava.examples;

import java.util.LinkedList;

public class ListNode extends LinkedList {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
